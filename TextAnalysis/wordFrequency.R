# List of songs files
list_of_files <- list.files(path = "./DataCollector/files/",
                            pattern = "\\.txt$",
                            full.names = TRUE)
texts <- list_of_files %>%
  set_names(.) %>%
  map_df(read_table2, .id= "FileName")

texts_tokens <- texts %>%
  unnest_tokens(word, X1)

noStopWords <- texts_tokens %>%
  anti_join(stopWords, by = "word")

noStopWords %>%
  count(word, sort = TRUE) %>%
  filter(n > 500) %>%
  mutate(word = reorder(word, n)) %>%
  ggplot(aes(word, n)) +
  geom_col() +
  xlab(NULL) +
  coord_flip()
